void main() {
  List<int> l = [14, 25, 88, 70, 90, 77, -45, 22, 99, 66, 15, 100];

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  //to find larget number
  int max = 0;
  for (int i = 0; i < l.length; i++) {
    if (max < l[i]) {
      max = l[i];
    }
  }
  print("max in   $l    is $max");
  //max in   [14, 25, 88, 70, 90, 77, -45, 22, 99, 66, 15, 100]    is 100

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  // to find minimum number in list
  int min = l[0];
  for (int i = 0; i < l.length; i++) {
    if (l[i] < min) {
      min = l[i];
    }
  }
  print("min in   $l   is $min");
  //min in   [14, 25, 88, 70, 90, 77, -45, 22, 99, 66, 15, 100]   is -45

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //to find largest and smallest odd number list
  int maxOdd = 0;
  int minOdd = 0;

  for (int i = 0; i < l.length; i++) {
    //for max
    if (l[i] % 2 != 0) {
      if (maxOdd < l[i]) {
        maxOdd = l[i];
      }
      //for min
      if (minOdd > l[i]) {
        minOdd = l[i];
      }
    }
  }
  print("min odd in   $l   is $minOdd");
  //min odd in   [14, 25, 88, 70, 90, 77, -45, 22, 99, 66, 15, 100]   is -45
  print("max odd in   $l   is $maxOdd");
  //max odd in   [14, 25, 88, 70, 90, 77, -45, 22, 99, 66, 15, 100]   is 99

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  //to find largest and smallest even number in list
  int maxEven = 0;
  int minEven = l[0];
  for (int i = 0; i < l.length; i++) {
    if (l[i] % 2 == 0) {
      if (maxEven < l[i]) {
        maxEven = l[i];
      }
      if (minEven > l[i]) {
        minEven = l[i];
      }
    }
  }
  print("min even in   $l   is $minEven");
  //min even in   [14, 25, 88, 70, 90, 77, -45, 22, 99, 66, 15, 100]   is 14
  print("max even in   $l   is $maxEven");
  //max even in   [14, 25, 88, 70, 90, 77, -45, 22, 99, 66, 15, 100]   is 100
}
