void main() {
  //swap three numbers
  /*
    a b c
    c a b
  */
  int a = 10;
  int b = 20;
  int c = 30;
  //logic
  int temp1 = c;
  int temp2 = a;
  c = b;
  a = temp1;
  b = temp2;
  print("a: $a"); //30
  print("b: $b"); //10
  print("c: $c"); //20

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  /*
    x y z
    y z x
   */
  int x = 10;
  int y = 20;
  int z = 30;
  //logic
  int t1 = x;
  int t2 = z;
  x = y;
  z = t1;
  y = t2;
  print("x: $x"); //20
  print("y: $y"); //30
  print("z: $z"); //10

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  //swap three numbers without using third variable
  /*
      p q r
      r p q
  */
  int p = 10;
  int q = 20;
  int r = 30;
  //logic
  int sum = p + q + r;
  int pq = p + q;
  int qr = q + r;
  int pr = p + r;
  p = sum - pq;
  q = sum - qr;
  r = sum - pr;
  print("p:$p"); //30
  print("q:$q"); //10
  print("r:$r"); //20

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  /*
      m n o
      n o m
  */
  int m = 10;
  int n = 20;
  int o = 30;
  int mno = m + n + o;
  int mo = m + o;
  int mn = m + n;
  int no = n + o;
  m = mno - mo;
  n = mno - mn;
  o = mno - no;
  print("m: $m"); //20
  print("n: $n"); //30
  print("o: $o"); //10
}
